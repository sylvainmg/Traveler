let id = 1;
for (let i = 0; i < hotels.length; i++) {
    const pays = hotels[i].pays;
    const code = obj.find((el) => el["NOM"] === pays);

    if (code) {
        csv += id + ","; //id
        id++;

        csv += code["CODE"] + ",";

        const dispo = ["AV", "NA"][Math.round(Math.random())];
        csv += dispo + ",";

        csv += hotels[i].HotelName + ",";
        csv += hotels[i].nb_etoile;
        csv += "\n";
    }
}

fs.writeFileSync("hotels.csv", csv);
